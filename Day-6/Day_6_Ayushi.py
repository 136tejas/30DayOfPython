# -> Question: Write a program to find ascii code of the given input character.
# -> Input: Take character input from the user and store it in a variable.
# -> Output: print the ASCII code of that input character.

# -> Example
# Input: A
# Output: 065
# Advance: Also print the binary value of it

n=input()
print(f"ASCII Code of {n} is {ord(n)}")
print(f"ASCII Code in Binary of {n} is {bin(ord(n))}")

