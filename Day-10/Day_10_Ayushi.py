# -> Question: Write a Program to sum n natural numbers.

# -> Input: Take number as input from the user and store it in a variable.

# -> Output: print the sum of n numbers

# -> Example
# Input: 5
# Output: 15

# Condition: Perform this program without using any loop.

# Expected Time complexity: O(1) 

